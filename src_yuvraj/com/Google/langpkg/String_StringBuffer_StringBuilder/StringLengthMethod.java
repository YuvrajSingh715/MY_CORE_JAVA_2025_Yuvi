package com.Google.langpkg.String_StringBuffer_StringBuilder;

public class StringLengthMethod {

	public static void main(String[] args) {
		
		String s = "asdfghjkl";
		System.out.println("Length of the string: "+s.length());
		//length() is applicable for the string objects;
		
		
		char[] ch = {'d','h','i','w','u'};
		System.out.println("Length of the char array: "+ch.length);
		//length variable is applicable for the arrays;
		
	}
}
